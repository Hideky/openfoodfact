def grey(content):
	return '\x1b[90m{}\x1b[0m'.format(content)

def red(content):
	return '\x1b[91m{}\x1b[0m'.format(content)

def green(content):
	return '\x1b[92m{}\x1b[0m'.format(content)

def yellow(content):
	return '\x1b[93m{}\x1b[0m'.format(content)

def blue(content):
	return '\x1b[94m{}\x1b[0m'.format(content)

def magenta(content):
	return '\x1b[95m{}\x1b[0m'.format(content)

def cyan(content):
	return '\x1b[96m{}\x1b[0m'.format(content)

def white(content):
	return '\x1b[97m{}\x1b[0m'.format(content)